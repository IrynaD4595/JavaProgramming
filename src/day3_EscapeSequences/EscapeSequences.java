package day3_EscapeSequences;
/*
Escape sequences: Must be given with double quote
\n: starts a new line
\t: paragraph space
\\: single back slash
 */
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#");
        System.out.println("---------------------------------------");


        System.out.println("Hello Cydeo \nHow are you all today? \nIts nice to see you all!");
        System.out.println("----------------------------------\n----------------------------");


        System.out.println("\tJava is cool programming language");

        System.out.println("-------------------------\n----------------");


        System.out.println("\\");  // in order to print one backward slash we net\ed to put 2 backward slash

        System.out.println("---------------------------------------");
        System.out.println("my favorite TV show is \"Friends\"");  //in order to print double quot



    }





}
